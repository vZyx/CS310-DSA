package Quadratic;// Selection sort in Java

import java.util.Arrays;

public class SelectionSort {

    public static void selectionSort(int[] array) {
        // Loop through each element in the array
        for (int i = 0; i < array.length; i++) {
            // Assume the current index is the minimum index
            int minIndex = i;

            // Find the index of the smallest element in the unsorted portion of the array
            for (int j = i+1; j < array.length; j++) {
                // If the current element is smaller than the element at minIndex, update minIndex
                if ( array[j] < array[minIndex] ) {
                    minIndex = j;
                }
            }
            // If minIndex is different from i, swap the elements at minIndex and i
            if (minIndex != i) {
                int temp = array[i];
                array[i] = array[minIndex];
                array[minIndex] = temp;
            }
        }
    }




    public static void main(String[] args) {

        int[] myArray = {4,2,6,5,1,3};

        selectionSort(myArray);

        System.out.println( Arrays.toString(myArray) );

        /*
            EXPECTED OUTPUT:
            ----------------
            [1, 2, 3, 4, 5, 6]

         */

    }

}

